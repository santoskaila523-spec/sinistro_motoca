function normalizarPlacaMotoca(string $placa): string {
    $placa = strtoupper(trim($placa));
    $placa = preg_replace('/[^A-Z0-9]/', '', $placa);
    return substr($placa, 0, 7);
}
function placaFormatoMercosulValido(string $placa): bool {
    $placa = normalizarPlacaMotoca($placa);
    return (bool) preg_match('/^[A-Z]{3}[0-9][A-Z][0-9]{2}$/', $placa);
}
function placaMotocaValida(string $placa): bool {
    $placaNormalizada = normalizarPlacaMotoca($placa);
    if ($placaNormalizada === '' || !preg_match('/^[A-Z]{3}[0-9][A-Z][0-9]{2}$/', $placaNormalizada) && !preg_match('/^[A-Z]{3}[0-9]{4}$/', $placaNormalizada)) {
        return false;
    }
    return true;
}
function placasMotocaPorBase(?string $base): array {
    global $PLACAS_MOTOCA_POR_BASE, $PLACAS_MOTOCA_VALIDAS;
    $baseNormalizada = strtolower(trim((string) $base));
    if ($baseNormalizada === '' || !isset($PLACAS_MOTOCA_POR_BASE[$baseNormalizada])) {
        return $PLACAS_MOTOCA_VALIDAS;
    }
    return $PLACAS_MOTOCA_POR_BASE[$baseNormalizada];
}
